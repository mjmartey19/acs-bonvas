// JavaScript code for switching languages (same as provided earlier)
const languageSelect = document.getElementById("language-select");

function translatePage(language) {
  document.getElementById("hero-heading").innerHTML = translations[language].heroHeading;
  document.getElementById("cta-button-text").textContent = translations[language].ctaButtonText;
  document.getElementById("scroll-down").textContent = translations[language].scrollDown;
  document.getElementById("honour-heading").textContent = translations[language].honourHeading;
  document.getElementById("honour-text").textContent = translations[language].honourText;
  document.getElementById("branding-text").textContent = translations[language].brandingText;

  // Features Section
  document.querySelector("#feature-first h2").textContent = translations[language].exclusiveTourOffer;
  document.querySelector("#feature-first p").textContent = translations[language].exclusiveTourDesc;
  

  // Price table section
  document.querySelector("#price-table h2").textContent = translations[language].exclusivePackages;
  document.querySelector("#price-table p").textContent = translations[language].exclusivePackagesDesc;
  

  // Standard Package
  document.querySelector("#price-table .col-md-4:nth-child(1) h4").textContent = translations[language].standardPackage;
  document.querySelector("#price-table .col-md-4:nth-child(1) p").innerHTML = `$<span class="display-2 font-weight-bold">${translations[language].standardPrice.split(" ")[0]}</span> USD.`; 
  document.querySelector("#price-table .col-md-4:nth-child(1) ul li").textContent = translations[language].standardDiscount;

  // Premium Package
  document.querySelector("#price-table__premium h4").textContent = translations[language].premiumPackage;
  document.querySelector("#price-table__premium p").innerHTML = `$<span class="display-2 font-weight-bold">${translations[language].premiumPrice.split(" ")[0]}</span> USD.`;
  document.querySelector("#price-table__premium ul li").textContent = translations[language].premiumDiscount;

  // Luxury Package
  document.querySelector("#price-table .col-md-4:nth-child(3) h4").textContent = translations[language].luxuryPackage;
  document.querySelector("#price-table .col-md-4:nth-child(3) p").innerHTML = `$<span class="display-2 font-weight-bold">${translations[language].luxuryPrice.split(" ")[0]}</span> USD.`;
  document.querySelector("#price-table .col-md-4:nth-child(3) ul li").textContent = translations[language].luxuryDiscount;

    // Update all "Book Now" buttons
    document.querySelectorAll(".book-now").forEach(button => {
        button.textContent = translations[language].bookNow;
    });


  // Contact section
  document.querySelector("#contact h2").textContent = translations[language].stayConnected;
  document.querySelector("#contact p").textContent = translations[language].contactInfo;
  document.querySelector("#contact ul li:nth-child(1) strong").textContent = translations[language].email;
  document.querySelector("#contact ul li:nth-child(2) strong").textContent = translations[language].phone;
  document.querySelector("#contact ul li:nth-child(4)").textContent = translations[language].website;

  // Footer copyright
  document.querySelector("#copyright .col-md-6").textContent = translations[language].copyright;

    // Update gift box content
  const giftBoxContainer = document.querySelector(".gift-box-container");
  giftBoxContainer.innerHTML = ""; // Clear existing items

translations[language].giftBoxContent.items.forEach(item => {
  const itemElement = document.createElement("div");
  itemElement.classList.add("gift-card");
  itemElement.innerHTML = `
    <img src="${item.image}" alt="${item.title}" class="card-icon">
    <h3>${item.title}</h3>
    <div class="desc">
      <p><strong>Highlight:</strong> ${item.highlight}</p>
      <p><strong>Purpose:</strong> ${item.purpose}</p>
    </div>
  `;
  giftBoxContainer.appendChild(itemElement);
});

}

// Function to set the selected language in local storage
function setLanguageInLocalStorage(language) {
  localStorage.setItem("selectedLanguage", language);
}

// Function to get the selected language from local storage
function getLanguageFromLocalStorage() {
  return localStorage.getItem("selectedLanguage") || "en"; // Default to English if no language is set
}

// Load the previously selected language from local storage
const selectedLanguage = getLanguageFromLocalStorage();
translatePage(selectedLanguage);
languageSelect.value = selectedLanguage;

// Listen for language selection change
languageSelect.addEventListener("change", function () {
  const selectedLanguage = this.value;
  translatePage(selectedLanguage);
  setLanguageInLocalStorage(selectedLanguage); // Save the selected language to local storage
});
